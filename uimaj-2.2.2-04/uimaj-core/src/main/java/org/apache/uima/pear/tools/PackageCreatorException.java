package org.apache.uima.pear.tools;

import org.apache.uima.InternationalizedException;

/**
 * Exception thrown if the PEAR package generation fails.
 */
public class PackageCreatorException extends InternationalizedException {
  private static final long serialVersionUID = 6261840563059646801L;

  /**
   * Creates a new <code>PackageCreatorException</code> with the specified message.
   * 
   * @param aResourceBundleName
   *          the base name of the resource bundle in which the message for this exception is
   *          located.
   * @param aMessageKey
   *          an identifier that maps to the message for this exception. The message may contain
   *          placeholders for arguments as defined by the
   *          {@link java.text.MessageFormat MessageFormat} class.
   * @param aArguments
   *          The arguments to the message. <code>null</code> may be used if the message has no
   *          arguments.
   */
  public PackageCreatorException(String aResourceBundleName, String aMessageKey,
          Object[] aArguments) {
    super(aResourceBundleName, aMessageKey, aArguments, null);
  }

  /**
   * Creates a new <code>PackageCreatorException</code> with the specified message.
   * 
   * @param aResourceBundleName
   *          the base name of the resource bundle in which the message for this exception is
   *          located.
   * @param aMessageKey
   *          an identifier that maps to the message for this exception. The message may contain
   *          placeholders for arguments as defined by the
   *          {@link java.text.MessageFormat MessageFormat} class.
   */
  public PackageCreatorException(String aResourceBundleName, String aMessageKey) {
    super(aResourceBundleName, aMessageKey, null, null);
  }

  /**
   * Creates a new <code>PackageCreatorException</code> with the specified message and cause.
   * 
   * @param aResourceBundleName
   *          the base name of the resource bundle in which the message for this exception is
   *          located.
   * @param aMessageKey
   *          an identifier that maps to the message for this exception. The message may contain
   *          placeholders for arguments as defined by the
   *          {@link java.text.MessageFormat MessageFormat} class.
   * @param aCause
   *          the original exception that caused this exception to be thrown, if any
   */
  public PackageCreatorException(String aResourceBundleName, String aMessageKey, Throwable aCause) {
    super(aResourceBundleName, aMessageKey, null, aCause);
  }

  /**
   * Creates a new <code>PackageCreatorException</code> with the specified message and cause.
   * 
   * @param aResourceBundleName
   *          the base name of the resource bundle in which the message for this exception is
   *          located.
   * @param aMessageKey
   *          an identifier that maps to the message for this exception. The message may contain
   *          placeholders for arguments as defined by the
   *          {@link java.text.MessageFormat MessageFormat} class.
   * @param aArguments
   *          The arguments to the message. <code>null</code> may be used if the message has no
   *          arguments.
   * @param aCause
   *          the original exception that caused this exception to be thrown, if any
   */
  public PackageCreatorException(String aResourceBundleName, String aMessageKey,
          Object[] aArguments, Throwable aCause) {
    super(aResourceBundleName, aMessageKey, aArguments, aCause);
  }

}
